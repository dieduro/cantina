            //Consultando a la DB para llenar de DATA los campos de la TABLA
            String sql2 = "SELECT a.id, a.desripcion, r.nombre, a.cantidad, a.precio FROM articulos a JOIN rubros r on a.rubro_id=r.id WHERE a.activo = '1'";
